"""UTCI (Universal Thermal Climate Index) — odczuwalna temperatura outdoor.

Wzór: aproksymacja wielomianowa Broede et al. (2012), 210 wspolczynnikow
(rozlozona symbolicznie z implementacji pythermalcomfort 4.0.2 — zweryfikowana
bit-w-bit). Cisnienie pary nasyconej: kanoniczny wzor Broede z log(tk) — zgodny
z fizyka (es(20C)=23.39 hPa). UWAGA: pythermalcomfort 4.0.2 uzywa tu log1p(tk)
(drobny blad, ~+0.9%); ta wersja jest poprawniejsza.

Wejscia: tdb [C], tr=Tmrt [C], v=wiatr 10 m [m/s], rh [%].
Zakres waznosci: tdb -50..50, (tr-tdb) -30..70, v 0.5..17 m/s.
Dok.: docs/PROJEKT_METEO.md (Watek 2).
"""
from __future__ import annotations
import math
from typing import Optional

V_MIN, V_MAX = 0.5, 17.0
DT_MIN, DT_MAX = -30.0, 70.0
TDB_MIN, TDB_MAX = -50.0, 50.0

_SAT_G = [-2836.5744, -6028.076559, 19.54263612, -0.02737830188,
          0.000016261698, 7.0229056e-10, -1.8680009e-13]


def sat_vapour_hpa(tdb: float) -> float:
    """Cisnienie pary nasyconej [hPa] (Broede, log(tk) — kanoniczne)."""
    tk = tdb + 273.15
    es = 2.7150305 * math.log(tk)
    for c, g in enumerate(_SAT_G):
        es += g * tk ** (c - 2)
    return math.exp(es) * 0.01


def vapour_pressure_kpa(tdb: float, rh: float) -> float:
    return sat_vapour_hpa(tdb) * (rh / 100.0) / 10.0


