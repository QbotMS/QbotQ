def _utci_poly(tdb: float, v: float, dt: float, pa: float) -> float:
    s = 0.0
    for c, a, b, d, e in _UTCI_TERMS:
        s += c * tdb ** a * v ** b * dt ** d * pa ** e
    return s


def utci_c(tdb: float, tr: float, v: float, rh: float) -> float:
    """UTCI [C]. tr=Tmrt. Wiatr NIE jest przycinany tutaj — patrz utci_valid()."""
    pa = vapour_pressure_kpa(tdb, rh)
    return _utci_poly(tdb, v, tr - tdb, pa)


def utci_valid(tdb: float, tr: float, v: float) -> bool:
    return (TDB_MIN <= tdb <= TDB_MAX and DT_MIN <= (tr - tdb) <= DT_MAX
            and V_MIN <= v <= V_MAX)


_CATS = [(-40.0, "ekstremalny stres zimna"), (-27.0, "bardzo silny stres zimna"),
         (-13.0, "silny stres zimna"), (0.0, "umiarkowany stres zimna"),
         (9.0, "lagodny stres zimna"), (26.0, "brak stresu termicznego"),
         (32.0, "umiarkowany stres ciepla"), (38.0, "silny stres ciepla"),
         (46.0, "bardzo silny stres ciepla")]


def utci_category(u: float) -> str:
    for thr, label in _CATS:
        if u < thr:
            return label
    return "ekstremalny stres ciepla"
